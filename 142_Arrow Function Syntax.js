// Arrow function with no parameters:
const greet = () => "Hello, World!";
// Arrow function with one parameter (no parentheses required):
const square = x => x * x;

// Arrow function with multiple parameters:
const add = (a, b) => a + b;
// Arrow function with a block of code:
const multiply = (a, b) => {
   const result = a * b;
   return result;
 };
 